--  DDL for Function BONUSCAL
--------------------------------------------------------

  CREATE OR REPLACE FUNCTION "KH"."BONUSCAL" (v_emp_id employee.emp_id%type)
return number
is
    v_sal employee.salary%type;
    v_bonus employee.bonus%type;
    cal_sal number;
    begin
        select salary, nvl(bonus,0)
        into v_sal, v_bonus
        from employee
        where emp_id = v_emp_id;

        cal_sal := (v_sal + v_sal * v_bonus) * 12;
        return cal_sal;
    end;

