--  DDL for View V_ACCOUNT
--------------------------------------------------------

  CREATE OR REPLACE FORCE VIEW "KH"."V_ACCOUNT" ("이름", "부서명", "직급", "월급", "연봉") AS 
  select emp_name, dept_title, job_name, salary, (salary + (salary*nvl(bonus, 0))) * 12 as "연봉"
    from employee e
    left join department on (dept_id = dept_code)
    left join job j on (e.job_code = j.job_code)
;
